# --- Creating the Interactive VPN Monitor Script ---
cat > /usr/local/bin/vpn-monitor.sh << 'EOL'
#!/bin/bash

CHECK_HOST="1.1.1.1"
TUN_INTERFACE="tun0"
CHECK_INTERVAL=5      
PAUSE_MINUTES=5       
SNOOZE_MINUTES=1      

STATE_FILE="/run/user/$(id -u)/vpn_monitor.state"

mkdir -p "/run/user/$(id -u)"
echo 0 > "$STATE_FILE"
LAST_STATE="UP"

echo "–°–ª—É–∂–±–∞ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ VPN –∑–∞–ø—É—â–µ–Ω–∞. –ò–Ω—Ç–µ—Ä—Ñ–µ–π—Å: $TUN_INTERFACE, –•–æ—Å—Ç: $CHECK_HOST"

while true; do
    # 1. –ü–†–û–í–ï–†–ö–ê –°–û–ï–î–ò–ù–ï–ù–ò–Ø
    if ping -c 1 -W 3 -I "$TUN_INTERFACE" "$CHECK_HOST" > /dev/null 2>&1; then
        # –°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –µ—Å—Ç—å
        if [ "$LAST_STATE" = "DOWN" ]; then
            echo "$(date): –°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ."
            notify-send -i network-transmit-receive "VPN –ú–æ–Ω–∏—Ç–æ—Ä" "–°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ"
            echo 0 > "$STATE_FILE"
        fi
        LAST_STATE="UP"
    else
        # –°–æ–µ–¥–∏–Ω–µ–Ω–∏—è –Ω–µ—Ç
        SNOOZE_UNTIL=$(cat "$STATE_FILE")
        CURRENT_TIME=$(date +%s)

        if [ "$CURRENT_TIME" -gt "$SNOOZE_UNTIL" ]; then
            if [ "$LAST_STATE" = "UP" ]; then
                 echo "$(date): –°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –ø–æ—Ç–µ—Ä—è–Ω–æ! –û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è."
            else
                 echo "$(date): –°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –≤—Å–µ –µ—â–µ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç. –ü–æ–≤—Ç–æ—Ä–Ω–∞—è –æ—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è."
            fi

            # 2. –û–¢–ü–†–ê–í–ö–ê –ò–ù–¢–ï–†–ê–ö–¢–ò–í–ù–û–ì–û –£–í–ï–î–û–ú–õ–ï–ù–ò–Ø
            ACTION=$(notify-send -u critical -t 15000 \
                --action="pause=–ü–∞—É–∑–∞ –Ω–∞ $PAUSE_MINUTES –º–∏–Ω" \
                --action="snooze=–ù–∞–ø–æ–º–Ω–∏—Ç—å —á–µ—Ä–µ–∑ $SNOOZE_MINUTES –º–∏–Ω" \
                "VPN –°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –ü–æ—Ç–µ—Ä—è–Ω–æ!" \
                "–ù–µ—Ç –æ—Ç–≤–µ—Ç–∞ –æ—Ç $CHECK_HOST —á–µ—Ä–µ–∑ $TUN_INTERFACE." 2>/dev/null)

            # 3. –û–ë–†–ê–ë–û–¢–ö–ê –î–ï–ô–°–¢–í–ò–Ø –ü–û–õ–¨–ó–û–í–ê–¢–ï–õ–Ø
            case "$ACTION" in
                "pause")
                    NEW_SNOOZE_TIME=$((CURRENT_TIME + PAUSE_MINUTES * 60))
                    echo "$NEW_SNOOZE_TIME" > "$STATE_FILE"
                    echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –Ω–∞ –ø–∞—É–∑–µ –Ω–∞ $PAUSE_MINUTES –º–∏–Ω—É—Ç."
                    ;;
                "snooze")
                    NEW_SNOOZE_TIME=$((CURRENT_TIME + SNOOZE_MINUTES * 60))
                    echo "$NEW_SNOOZE_TIME" > "$STATE_FILE"
                    echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ª–æ–∂–µ–Ω–æ –Ω–∞ $SNOOZE_MINUTES –º–∏–Ω—É—Ç—É."
                    ;;
                *)
                    # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–∞–∫—Ä—ã–ª —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ. –°—Ç–∞–≤–∏–º –∫–æ—Ä–æ—Ç–∫—É—é –ø–∞—É–∑—É –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é.
                    NEW_SNOOZE_TIME=$((CURRENT_TIME + 60))
                    echo "$NEW_SNOOZE_TIME" > "$STATE_FILE"
                    ;;
            esac
        else
            : # –ù–∞—Ö–æ–¥–∏–º—Å—è –≤ —Ä–µ–∂–∏–º–µ "–ø–∞—É–∑—ã", –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º
        fi
        LAST_STATE="DOWN"
    fi

    sleep "$CHECK_INTERVAL"
done
EOL

# --- Creating the Notification Script ---
cat > /usr/local/bin/killswitch-notify.sh << EOL
#!/bin/bash
# This script sends a notification about the Kill Switch status.
ACTIVE_USER=$(loginctl list-sessions | awk 'NR==2 {print $3}')
    
if [ -n "\$ACTIVE_USER" ]; then
    # Check Kill Switch status
    if sudo ufw status verbose | grep -q "Default:.*deny (incoming).*deny (outgoing)"; then
        MESSAGE="Kill Switch is ACTIVE. All traffic is blocked."
        ICON="network-warning"
    else
        MESSAGE="WARNING: Kill Switch is NOT active!"
        ICON="network-warning"
    fi
    DISPLAY=:0 su \$ACTIVE_USER -c "notify-send -u critical -i '\$ICON' '\$MESSAGE'"
    # Log the notification
    LOG_FILE="/var/log/killswitch-notify.log"
    echo "$(date): Notification sent to user \$ACTIVE_USER" >> \$LOG_FILE
fi
EOL


